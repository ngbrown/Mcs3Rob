namespace Mcs3Rob
{
    public enum RobDirection
    {
        /// <summary>
        /// The value increments with the '+' key and decrements with the '-' key
        /// </summary>
        Normal = 0,

        /// <summary>
        /// The value decrements with the '+' key and increments with the '-' key
        /// </summary>
        Reverse = 1,
    }
}